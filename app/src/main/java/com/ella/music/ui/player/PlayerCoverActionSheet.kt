            lyricSourceMode = lyricSourceMode,
            lyricParserEngine = lyricParserEngine,
            lyricLayoutProfile = lyricLayoutProfile,
            lyricFontScale = lyricFontScale,
            lyricSecondaryFontScale = lyricSecondaryFontScale,
