            onAddToPlaylist = onAddToPlaylist,
            onAddToQueue = onAddToQueue,
            onPlayNext = onPlayNext,
            onShare = onShareSong,
            onSetRating = onSetRating,
            onAiInterpret = onAiInterpret,
